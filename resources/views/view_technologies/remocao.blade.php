					<a class="collapsed" role="button" title="" data-toggle="collapse" 
					data-parent="#accordion" href="#collapse4" aria-expanded="true" 
					aria-controls="collapse4">
					  Gestão de materiais/resíduos com Amianto
					</a>
				  </h3>
				</div>
				<div id="collapse4" class="panel-collapse collapse" role="tabpanel" 
				aria-labelledby="heading4">
				  <div class="panel-body px-3 mb-4">
					<p>A remoção, acondicionamento e eliminação dos resíduos que contêm 
						amianto devem ser alvo de procedimentos adequados face à avaliação 
						de risco previamente efetuada, pois poderão constituir fontes de 
						exposição ocupacional e ambiental, caso não sejam observadas as 
						medidas regulamentares adequadas.</p>

					<p>Na remoção de materiais contendo amianto deve ser cumprido o 
						<a href="https://dre.pt/pdf1sdip/2007/07/14100/0468904696.pdf" target="_blank" rel="noopener">Decreto-Lei nº 266/2007 de 24 de julho</a>, relativo à proteção 
						sanitária dos trabalhadores contra os riscos de exposição ao 
						amianto durante o trabalho. É ainda obrigatória a notificação
						 à Autoridade para as Condições de Trabalho das atividades no
						  exercício das quais o trabalhador está, ou pode estar, sujeito
						   a exposição a poeiras ou partículas de amianto ou de materiais 
						   que contenham amianto.</p>

						<p>Os trabalhos de remoção devem ser acompanhados de recolha 
							de amostras de ar para avaliação da contaminação do ar 
							por fibras respiráveis para controlo/garantia da sua adequada 
							execução. No final dos trabalhos deverá ser efetuada nova 
							avaliação para garantir a conformidade com o valor de concentração
							 de 0,01fibra/cm3 preconizado pela Organização Mundial de Saúde
							  como indicador de área limpa.</p>

						<p>Quanto à gestão de resíduos de construção e demolição (RCD),
							 deve cumprir-se o disposto no 
							 <a href="https://dre.pt/pdf1sdip/2008/03/05100/0156701574.pdf" target="_blank" rel="noopener">Decreto-Lei nº 46/2008, de 12 de março</a>,
							  de 12 de março, que preconiza, entre outros aspetos,
							   a publicação de normas para a correta remoção dos
								materiais contendo amianto e para o acondicionamento, 
								transporte e gestão dos respetivos RCD gerados, através 
								de Portaria dos membros do Governo responsáveis pelas
								 áreas do ambiente, da saúde e do trabalho.</p>

						<p>A <a href="https://dre.pt/pdf1sdip/2014/02/03300/0143501442.pdf" target="_blank" rel="noopener">Portaria nº 40/2014, de 17 de fevereiro</a>, de 17 de fevereiro, estabelece as normas
							 para a correta remoção dos materiais contendo amianto e para
							  o acondicionamento, transporte e gestão dos respetivos resíduos
							   de construção e demolição gerados, tendo em vista a proteção
								do ambiente e da saúde humana.</p>
								
						<p>No que se reporta à fiscalização do cumprimento do disposto 
							no <a href="https://dre.pt/pdf1sdip/2008/03/05100/0156701574.pdf" target="_blank" rel="noopener">Decreto-Lei nº 46/2008</a>, que estabelece o regime das 
							operações de gestão de resíduos resultantes de obras ou demolições
							 de edifícios ou de derrocadas, designados por resíduos de 
							 construção e demolição (RCD), esta é remetida para a Inspeção-Geral 
							 da Agricultura, do Mar, do Ambiente e do Ordenamento do Território,
							  para as Comissões de Coordenação e Desenvolvimento Regional, bem
							   como para os municípios e para as autoridades policiais.</p>
					
