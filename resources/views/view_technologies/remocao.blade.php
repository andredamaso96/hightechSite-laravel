			<h2>Perguntas frequentes sobre o Amianto</h2>
			<div class="panel-group" id="accordion" role="tablist" aria-multiselectable="true">
			  <div class="panel panel-default">
				<div class="panel-heading p-3 mb-3" role="tab" id="heading0">
				  <h3 class="panel-title">
					<a class="collapsed" role="button" title="" data-toggle="collapse" data-parent="#accordion" href="#collapse0" aria-expanded="true" aria-controls="collapse0">
						O que é o Amianto?
					</a>
				  </h3>
				</div>
				<div id="collapse0" class="panel-collapse collapse" role="tabpanel" aria-labelledby="heading0">
				  <div class="panel-body px-3 mb-4">

					<p>O amianto ou asbestos é a designação comercial utilizada para a variedade
						fibrosa de seis minerais metamórficos de ocorrência natural.</p>

				   <p>Devido às suas propriedades (elasticidade, resistência mecânica, incombustibilidade, 
					   bom isolamento térmico e acústico, elevada resistência a altas temperaturas, 
					   aos produtos químicos, à putrefação e à corrosão) o amianto teve, no passado, 
					   numerosas aplicações nomeadamente na indústria da construção, encontrando-se 
					   presente em diversos tipos de materiais tais como: telhas de fibrocimento, 
					   revestimentos e coberturas de edifícios, gessos e estuques, revestimentos à 
					   prova de fogo, revestimentos de tetos falsos, isolamentos térmicos e acústicos, 
					   entre outros. Na Europa foi particularmente utilizado entre 1945 e 1990.</p>

					   <p>Em Portugal, foi proibida a utilização/comercialização 
						   de amianto e/ou produtos que o contenham a partir de 1 de janeiro de 2005, 
						   de acordo com o disposto na Diretiva 2003/18/CE transposta para o direito interno 
						   através do <a href="https://dre.pt/application/dir/pdf1sdip/2005/06/119A00/39373939.pdf">Decreto-Lei 
							   nº 101/2005, de 23 de junho.</a></p>
