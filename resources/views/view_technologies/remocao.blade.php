						   de exposição (VLE) é fixado em 0,1 fibra/cm3 para todos
						    os tipos de fibras de amianto.</p>

					<p>No caso da exposição da população em geral, o nível de concentração
						 das fibras de amianto em suspensão no ar deverá ser inferior a 0,01 
						 fibra/cm3, valor considerado pela Organização Mundial da Saúde como 
						 indicador de área limpa.</p>

					<p>Estes referenciais são estabelecidos atendendo a que o Homem pode ser 
						exposto ao amianto por 3 vias: via cutânea, por inalação e por ingestão, 
						sendo a via preponderante a respiratória.</p>

					

