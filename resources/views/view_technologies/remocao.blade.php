			  <div class="panel panel-default">
				<div class="panel-heading p-3 mb-3" role="tab" id="heading1">
				  <h3 class="panel-title">
					<a class="collapsed" role="button" title="" data-toggle="collapse" data-parent="#accordion" href="#collapse1" aria-expanded="true" aria-controls="collapse1">
					  Riscos relativos ao Amianto
					</a>
				  </h3>
				</div>
				<div id="collapse1" class="panel-collapse collapse" role="tabpanel" aria-labelledby="heading1">
				  <div class="panel-body px-3 mb-4">
					<p>O perigo do amianto decorre sobretudo da inalação das fibras 
						libertadas para o ar.</p>
					<p>Regra geral, a presença de amianto em materiais de construção 
						representa um baixo risco para a saúde, desde que o material 
						esteja em bom estado de conservação, não seja friável e não esteja 
						sujeito a agressões diretas. Qualquer atividade que implique a 
						quebra da integridade do material (corte, perfuração, quebra, etc.) 
						aumenta substancialmente o risco de libertação de fibras para o ar ambiente.</p>

					<p>Quando se suspeite da existência de material com amianto e com risco de libertação 
						de fibras para o ar, só com medições feitas com equipamento adequado e por 
						técnicos especializados é que é possível a determinação destas fibras e da 
						sua concentraçã</p>

					<p>Neste contexto, a confirmação da presença de amianto em determinado material 
						deverá ser feita através de análise em laboratório. Confirmada a presença 
						de amianto será necessário proceder à avaliação da contaminação do ar por 
						fibras respiráveis que requer a intervenção de técnicos com formação especializada 
						e o recurso a equipamento adequado.</p>
				  </div>
				</div>
			  </div>
			  
			  <div class="panel panel-default">
				<div class="panel-heading p-3 mb-3" role="tab" id="heading2">
				  <h3 class="panel-title">
					<a class="collapsed" role="button" title="" data-toggle="collapse" data-parent="#accordion" href="#collapse2" aria-expanded="true" aria-controls="collapse2">
					  Doenças associadas ao Amianto
					</a>
				  </h3>
				</div>
				<div id="collapse2" class="panel-collapse collapse" role="tabpanel" aria-labelledby="heading2">
				  <div class="panel-body px-3 mb-4">
					<p>As diferentes variedades de amianto são agentes cancerígenos, devendo a 
						exposição a qualquer tipo de fibra de amianto ser reduzida ao mínimo.</p>
					<p>As doenças associadas ao amianto são, em regra, resultantes da exposição
						 profissional, em que houve inalação das fibras respiráveis. Estas fibras 
						 microscópicas podem depositar-se nos pulmões e aí permanecer por muitos anos,
						  podendo vir a provocar doenças, vários anos ou décadas mais tarde.</p>
					<p>A exposição ao amianto pode causar as seguintes doenças: asbestose, mesotelioma, 
						cancro do pulmão (o fumo do tabaco poderá ser uma variável de confundimento,
						 agravando a evolução da doença) e ainda cancro gastrointestinal.</p>
				  </div>
				</div>
			  </div>
			  
			  <div class="panel panel-default">
				<div class="panel-heading p-3 mb-3" role="tab" id="heading3">
				  <h3 class="panel-title">
					<a class="collapsed" role="button" title="" data-toggle="collapse" data-parent="#accordion" href="#collapse3" aria-expanded="true" aria-controls="collapse3">
					  Materiais de construção suscetíveis de conter Amianto
					</a>
				  </h3>
				</div>
				<div id="collapse3" class="panel-collapse collapse" role="tabpanel" aria-labelledby="heading3">
				  <div class="panel-body px-3 mb-4">
					<p>Devido ao seu baixo custo e às suas propriedades, tais como de resistência mecânica, 
						de isolamento térmico, elétrico, acústico e de proteção contra o fogo, o amianto
						 teve diversas aplicações.</p>

					<p>Na indústria da construção civil, o amianto foi 
						utilizado nos seguintes elementos e materiais de construção:</p>
						<ul>
							<li>Pavimentos;</li>
							<li>Placas de teto falso;</li>
							<li>Produtos e materiais de revestimento e enchimento;</li>
							<li>Portas corta-fogo;</li>
							<li>Portas de courettes;</li>
							<li>Paredes divisórias pré-fabricadas;</li>
							<li>Elementos pré-fabricados constituídos por fibrocimento;</li>
							<li>Tijolos refratários;</li>
							<li>Telhas;</li>
							<li>Pintura texturizada;</li>
							<li>Caldeiras (revestimentos e apoios);</li>
							<li>Impermeabilização de coberturas e caleiras.</li>
						
						  </ul>

						  <p>Em termos de utilizações em casas de habitação, o
							   amianto friável raramente foi usado, sendo no entanto possível ser encontrado em:</p>

							   <ul>
								<li>Isolamento de tubagens de água quente;</li>
								<li>PIsolamento de antigos aquecedores domésticos;</li>
								<li>Isolamento de fogões;</li>
								<li>Materiais de isolamento de tetos.</li>
							
							
							  </ul>

				  </div>
				</div>
